function cambiarIdioma(lang) {
  const t = traducciones[lang];
  if (!t) return;
    document.querySelectorAll('[data-i18n]').forEach(el => {
    const clave = el.getAttribute('data-i18n');
    if (t[clave]) el.innerHTML = t[clave];
  });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const clave = el.getAttribute('data-i18n-html');
    if (t[clave]) el.innerHTML = t[clave];
  });
  
  if (document.getElementById("contenedor-noticias") && typeof window.reiniciarNoticiasDesdeI18n === "function") {
  window.reiniciarNoticiasDesdeI18n(lang);
}
}

document.addEventListener('DOMContentLoaded', () => {
  const selector = document.getElementById('lang-selector');
  const idiomaGuardado = localStorage.getItem('idioma') || 'es'; 
  cambiarIdioma(idiomaGuardado);

  if (selector) {
    selector.value = idiomaGuardado; 
    selector.addEventListener('change', function () {
    const idioma = this.value;
    cambiarIdioma(idioma);
    localStorage.setItem('idioma', idioma);

  const pathname = window.location.pathname;
  if (pathname.includes('detalle.html')) {
    const params = new URLSearchParams(window.location.search);
    const id = params.get('id');
    const fuente = params.get('fuente') || 'sebastiane';
    if (id && window.mostrarDetalle) {
      window.mostrarDetalle(id, idioma, fuente);
    }
  }

    if (pathname.includes('ver-noticia.html') && typeof renderizarNoticia === "function") {
      renderizarNoticia(idioma);
    }

    if (pathname.includes('encuentros.html')) {
      document.dispatchEvent(new CustomEvent("cambioIdioma", { detail: { idioma } }));
    }

    if (pathname.includes('ver-encuentro.html')) {
      $("#contenido-encuentro").empty();
      $.getScript('ver-encuentro.js');
    }
    if (pathname.includes('revistas.html')) {
      const contenedor = document.getElementById("lista-revistas");
      if (contenedor) contenedor.innerHTML = "";

      $.getScript('revista.js').done(() => {
        if (typeof cargarRevistas === "function") cargarRevistas();
        if (typeof cargarDocumentos === "function") cargarDocumentos();
      });
    }
  });
  }
});
